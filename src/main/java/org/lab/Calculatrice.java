package org.lab;

public class Calculatrice {

    public Calculatrice() {
        System.out.println("Calculatrice");
    }

    public double somme(double a, double b) {
        return a + b;
    }

    public double soustraction(double a, double b) {
        return a - b;
    }

    public double product(double a, double b) {
        return a * b;
    }
}
